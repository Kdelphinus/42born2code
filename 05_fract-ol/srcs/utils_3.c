int	ft_strcmp(char *s1, char *s2)
{
	while (1)
	{
		if (*s1 != *s2)
			return (0);
		if (*s1 == 0 && *s2 == 0)
			return (1);
		s1++;
		s2++;
	}
}

int	ft_strlen(char *s)
{
	int	i;

	i = 0;
	while (*s)
	{
		s++;
		i++;
	}
	return (i);
}
