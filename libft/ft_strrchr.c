char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*cs;

	i = 0;
	cs = (char *)s;
	while (cs[i])
		i++;
	if (c == 0)
		return (cs + i);
	while (--i >= 0)
	{
		if (cs[i] == c)
			return (cs + i);
	}
	return (0);
}
